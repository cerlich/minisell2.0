#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	char	*new_str;

	i = 0;
	while (str[i])
		i++;
	if (!(new_str = (char *)malloc((i + 1) * sizeof(*new_str))))
		return (NULL);
	i = 0;
	while (str[i])
	{
		new_str[i] = str[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
